let loadcoupon=()=>{
    document.getElementById('search_bar').style.opacity="0.3"
    document.getElementById('banner_section').style.opacity="0.3";
    document.getElementById('coupon').style.display='block'
    document.getElementById('services1').style.opacity="0.3"
}
let closecoupon=()=>{
    document.getElementById('coupon').style.display='none';
    document.getElementById('search_bar').style.opacity="1"
    document.getElementById('banner_section').style.opacity="1"
    document.getElementById('services1').style.opacity="1"

}